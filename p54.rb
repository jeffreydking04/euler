def is_four_of_a_kind?(hand)
  array = get_values(hand)
  array.sort!
  array1 = array[0..3]
  array2 = array[1..4]
  array1.uniq!
  array2.uniq!
  return (array2.size == 1 || array1.size == 1 )
end

def is_full_house?(hand)
  array = get_values(hand)
  array.uniq!
  return false if array.size != 2
  !is_four_of_a_kind?(hand)  
end

def is_three_of_a_kind?(hand)
  array = get_values(hand)
  array.uniq!
  return false if array.size != 3
  array = get_values(hand).sort!
  array1 = array[0..2]
  array2 = array[1..3]
  array3 = array[2..4]
  array1.uniq!
  array2.uniq!
  array3.uniq!
  array1.size == 1 || array2.size == 1 || array3.size == 1
end

def is_two_pair?(hand)
  array = get_values(hand)
  array.uniq!
  return false if array.size != 3
  !is_three_of_a_kind?(hand)
end

def is_one_pair?(hand)
  array = get_values(hand)
  array.uniq!
  array.size == 4
end

def get_highest_high_card_hand(hand1, hand2)
  array1 = get_values(hand1)
  array2 = get_values(hand2)
  array1.sort!
  array2.sort!
  (0..4).each do |x|
    return hand1 if array1[4-x] > array2[4-x]
    return hand2 if array2[4-x] > array1[4-x]
  end
end

